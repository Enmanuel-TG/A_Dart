// variables.dart

void main() {
  // ===== 1. Variables and Data Types =====

  // String: Stores text
  String name = 'Noa';
  String greeting = 'Hello, world!';

  // int: Stores integer numbers
  int age = 21;
  int birthYear = 2003;

  // double: Stores decimal numbers
  double height = 1.78;
  double weight = 70.5;

  // bool: Stores boolean values (true or false)
  bool isStudent = true;
  bool hasJob = false;

  // Print the values
  print('Name: $name');
  print('Greeting: $greeting');
  print('Age: $age');
  print('Birth Year: $birthYear');
  print('Height: $height');
  print('Weight: $weight');
  print('Is Student?: $isStudent');
  print('Has Job?: $hasJob');
}
